package var;

public class DOUBLE {
	public static void main(String[] args) {
		//부동소수점을 표현할때 사용하는 타입 (부동 : 떠서 움직이는 소수점) <->고정소수점
		//float: 짧은 소수점
		//double: 긴 소수점 (기본형임) * 50000000000
		float a= 1.12344f; // float는 f를 붙어야함
		System.out.println(a);
		double b = 35.35335353535;
		System.out.println(b);
	}//main
}//class
