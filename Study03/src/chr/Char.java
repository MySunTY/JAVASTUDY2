package chr;

public class Char {
	public static void main(String[] args) {
		//char : 케릭터 - 문자 하나를 저장할 수 있는 규격
		//문자 하나를 사용할때 ' 를 붙인다 (ex '가')
		//char에는 ""를 넣을 수 없다
		//''한글자만 들어갈 수 있다
		char a;
		a='안';
		System.out.print(a);
		char b;
		b='녕';
		char x;
		x=' ';
		char c;
		c='하';
		char d;
		d='세';
		char e;
		e='요';
		char f;
		f='.';
		System.out.print(b);
		System.out.print(x);
		System.out.print(c);
		System.out.print(d);
		System.out.print(e);
		System.out.println(f);
		
		char A;
		A='안';
		System.out.print(A);
		A='녕';
		System.out.print(A);
		A='하';
		System.out.print(A);
		A='세';
		System.out.print(A);
		A='요';
		System.out.print(A);
		A='.';
		System.out.print(A);
		
	}
}
