package doub;

public class DOUBLE {
	public static void main(String[] args) {
		//float : 짧은 소수점을 표기할 수 있는 규격
		//통상적으로 소수점이하 7자리까지 포함가능
		//짧은소수점표기할때 f를 붙여서 표기
		
		
		//double: 긴 소수점을 표기할 수 있는 규격
		//통상적으로 소수점이하 15자리까지 포함가능
		//x 500000000 일단 소수점을 적으면 double로 본다
		//d를 붙여도 되고 안붙여도 가능
		double a=0.35353535;
		System.out.println(a);
		float b=0.3535f;
		System.out.println(b);
	}//main
}//class
